#include<stdio.h>
int main()
{
    int i,j,k=1,a[10][11];
    printf("        ");
    for(i=1;i<=10;i++)
    printf("%8d",i);
    printf("\n");
    printf("-------------------------------------------------------------------------------------------------------------------------------------------------------------\n");
    for(i=0;i<10;i++)
    {
        a[i][0]=i+1;
        for(j=1;j<=10;j++)
        {
            a[i][j]=j*k;
            
        }
        k++;
    }
    for(i=0;i<10;i++)
    {
        printf("%4d   |",a[i][0]);
        for(j=1;j<=10;j++)
        {
            printf("%8d",a[i][j]);
        }
        printf("\n");
    }


}
